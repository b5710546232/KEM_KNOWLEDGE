export const getSeason = (season)=>{
  if (season=="inSeason"){
    return "In Season"
  }else {
    return "Double Season"
  }
}
export const getSensitivity = (sensitivity)=>{
  if (sensitivity="sensitivity"){
    return "Sensitivity"
  }else {
    return "Non-Sensitivity"
  }
}
export const getRiceType = (type)=>{
  if (type=="nonGlutinous"){
    return "Non-Glutinous"
  }else {
    return "Glutinous"
  }
}
